import SwiftUI

extension Color {
    struct MyTheme {
        static var purpleColor: Color {
            return Color("ColorPurple")
        }
        
        static var yellowColor: Color {
            return Color("ColorYellow")
        }
        
        static var beigeColor: Color {
            return Color("ColorBeige")
        }
    }
}
